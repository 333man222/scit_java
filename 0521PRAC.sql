--5
SELECT
    UPPER(FIRST_NAME)FIRST_NAME, UPPER(LAST_NAME)LAST_NAME
FROM 
    EMPLOYEES;
--6    
SELECT 
    FIRST_NAME, LAST_NAME, REPLACE(PHONE_NUMBER, '.','-') AS PHONENO
FROM 
    EMPLOYEES;


--7
SELECT
    FIRST_NAME, SALARY, ROUND(SALARY / 30, 1) AS 일일급여
FROM 
    EMPLOYEES;
    
--8    
SELECT
    DEPARTMENT_ID, HIRE_DATE, HIRE_DATE + 100 AS 근무100일째날
FROM 
    EMPLOYEES;
    
--9
SELECT
    FIRST_NAME, DEPARTMENT_ID, HIRE_DATE, CEIL(SYSDATE - HIRE_DATE) AS근무일수
FROM 
    EMPLOYEES
ORDER BY HIRE_DATE DESC;


--10
SELECT
    DEPARTMENT_ID, HIRE_DATE, FLOOR(MONTHS_BETWEEN(SYSDATE, HIRE_DATE)) AS 근속개월수
FROM 
    EMPLOYEES;
    
    
--11    
SELECT
    FIRST_NAME, DEPARTMENT_ID, TO_CHAR(HIRE_DATE, 'YYYY"년" MM"월" DD"일" DAY')  AS 입사일
FROM 
    EMPLOYEES;

--12
SELECT
    FIRST_NAME, DEPARTMENT_ID, HIRE_DATE
FROM
    EMPLOYEES
WHERE
    HIRE_DATE LIKE '__/03/__'
ORDER BY HIRE_DATE    ;
    
