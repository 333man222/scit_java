    FIRST_NAME, SUBSTR(PHONE_NUMBER, 1, 3)
FROM
    EMPLOYEES;
    
--2    
SELECT
    FIRST_NAME, LAST_NAME, LENGTH(FIRST_NAME)+LENGTH(LAST_NAME) AS 이름글자수
FROM
    EMPLOYEES;
    
SELECT
    FIRST_NAME, LAST_NAME, LENGTH(FIRST_NAME || LAST_NAME) AS이름글자수
FROM
    EMPLOYEES;


--3
SELECT
    FIRST_NAME, LAST_NAME, SUBSTR(first_name, 0, 1) ||','||SUBSTR(last_name, 0, 1) AS 첫글자
    
FROM
    EMPLOYEES;
    
