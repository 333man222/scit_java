
public class Third {
	public static void main(String[] args) {
		int number; //number라는 이름의 변수를 선언
		int value;
		number = 12; // = 대입한다는 뜻 오른쪽 데이터를 왼쪽 변수에
		value = 34;
		System.out.println(number+value); // 정수 or 값 or 상수==>변수에 담아서 사용
		
	}
}
